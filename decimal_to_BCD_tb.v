module tb_decimal_to_BCD();
reg [9:0] D;
wire [3:0] Y;
decimal_to_BCD d2b1(
    .D(D),
    .Y(Y)
);
initial begin
    D = 10'b0000000000; #10;
    D = 10'b0000000001; #10;
    D = 10'b0000000010; #10; 
    D = 10'b0000000100; #10;
    D = 10'b0000001000; #10;
    D = 10'b0000010000; #10;
    D = 10'b0000100000; #10;
    D = 10'b0001000000; #10;
    D = 10'b0010000000; #10;
    D = 10'b0100000000; #10;
    D = 10'b1000000000; #10;
end
endmodule